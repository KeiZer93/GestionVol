        <h2>Le Boeing 747</h2>
        <p>Le Boeing 747, surnommé "Jumbo", est un avion emblématique qui a marqué l'histoire de l'aviation. Il a été conçu par la société Boeing et est devenu l'un des avions de ligne les plus populaires et reconnaissables au monde. Utilisé principalement pour les vols long-courriers, le 747 a révolutionné l'industrie aérienne.</p>
