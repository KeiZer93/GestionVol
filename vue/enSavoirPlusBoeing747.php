    <section>
        <h3>Histoire du Boeing 747</h3>
        <p>Le Boeing 747 a été conçu pour répondre aux besoins croissants du transport aérien international à grande échelle. Il a été le premier avion de ligne à être équipé de deux ponts, permettant de transporter un grand nombre de passagers. Le premier vol commercial a eu lieu en 1970, et depuis, le 747 est devenu un symbole de l'aviation moderne.</p>
        <p>En dépit de sa taille imposante, le Boeing 747 est apprécié pour sa capacité à transporter de manière efficace et confortable un grand nombre de passagers sur des trajets longs, réduisant ainsi les coûts pour les compagnies aériennes.</p>
    </section>

    <section>
        <h3>Les versions du Boeing 747</h3>
        <p>Au fil des ans, plusieurs versions du Boeing 747 ont été développées, chacune offrant des améliorations et des caractéristiques spécifiques. Ces modèles incluent :</p>
        <ul>
            <li><strong>Boeing 747-100 :</strong> Le modèle original, lancé en 1970.</li>
            <li><strong>Boeing 747-200 :</strong> Une version améliorée avec une plus grande portée.</li>
            <li><strong>Boeing 747-400 :</strong> Un modèle à quatre moteurs, plus économique et doté de nouvelles technologies de navigation.</li>
            <li><strong>Boeing 747-8 :</strong> Le dernier modèle, introduit en 2012, avec des moteurs plus efficaces et une capacité augmentée.</li>
        </ul>
