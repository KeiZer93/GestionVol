        <h3>Histoire du Concorde</h3>
        <p>Le Concorde, fruit de la collaboration entre la France et le Royaume-Uni, est l'un des plus célèbres avions supersoniques de l'histoire. Bien qu'il ait été retiré du service commercial en 2003, il reste une légende de l'aviation pour sa vitesse époustouflante et son design distinctif.</p>
    </section>

    <section>
        <h3>Les performances du Concorde</h3>
        <p>Le Concorde était capable de franchir le mur du son, offrant des vols transatlantiques en un temps record. En raison de sa vitesse, il a également eu des défis techniques, notamment la gestion de la chaleur générée à grande vitesse. Cependant, il reste une prouesse de l'ingénierie aéronautique.</p>
